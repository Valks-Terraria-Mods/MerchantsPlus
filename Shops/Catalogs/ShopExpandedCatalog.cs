    private static readonly Dictionary<int, string[]> ShopNamesByMerchant = BuildShopNames();

    private static Dictionary<int, string[]> BuildShopNames()
    {
        Dictionary<int, string[]> namesByMerchant = new();

        foreach ((int merchantNpcId, ShopPage[] pages) in PagesByMerchant)
        {
            string[] names = new string[pages.Length];
            for (int i = 0; i < pages.Length; i++)
            {
                names[i] = pages[i].ShopName;
            }
            namesByMerchant[merchantNpcId] = names;
        }

        return namesByMerchant;
    }

    public static IReadOnlyList<string> GetShopNames(int merchantNpcId)
    {
        return ShopNamesByMerchant.TryGetValue(merchantNpcId, out string[] names)
            ? names
            : Array.Empty<string>();
    }

    public static bool TryGetPage(int merchantNpcId, string shopName, out ShopPage page)
    {
        if (PagesByMerchant.TryGetValue(merchantNpcId, out ShopPage[] pages))
        {
            for (int i = 0; i < pages.Length; i++)
            {
                if (pages[i].ShopName == shopName)
                {
                    page = pages[i];
                    return true;
                }
            }
        }

        page = default;
        return false;
    }
